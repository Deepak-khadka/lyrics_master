     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       $this->validate($request,[
           'name' => 'required','max:20','min:3',
       ]);
       $genre = new Genre();
       $genre->name = $request->input('name');
      if( $genre->save()){
          $genre = Genre::all();
          return  redirect('genre.index')->with('success','Successfully Genere has been added');
      }
      else{
          return redirect()->back()->with('error','Error to store Genere in the database');
      }
    }

